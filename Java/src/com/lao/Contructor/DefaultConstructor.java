package com.lao.Contructor;

public class DefaultConstructor { //default constructor throw value as string null value, int 0 
	String student_name;
	int rollno;
	long number;
	float dowble;
	boolean string;

	public static void main(String[] args) {
		DefaultConstructor constructor=new DefaultConstructor();
		System.out.println(constructor.student_name);
		System.out.println(constructor.rollno);
		System.out.println(constructor.number);
		System.out.println(constructor.dowble);
		System.out.println(constructor.string);
		

	}

}
